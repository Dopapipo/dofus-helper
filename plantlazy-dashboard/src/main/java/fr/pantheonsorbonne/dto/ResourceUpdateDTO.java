package fr.pantheonsorbonne.dto;

public class ResourceUpdateDTO {

    private int updatedWater;
    private int updatedEnergy;
    private int updatedFertilizer;
    private int updatedMoney;



    public int getUpdatedWater() {
        return updatedWater;
    }

    public void setUpdatedWater(int updatedWater) {
        this.updatedWater = updatedWater;
    }

    public int getUpdatedEnergy() {
        return updatedEnergy;
    }

    public void setUpdatedEnergy(int updatedEnergy) {
        this.updatedEnergy = updatedEnergy;
    }

    public int getUpdatedFertilizer() {
        return updatedFertilizer;
    }

    public void setUpdatedFertilizer(int updatedFertilizer) {
        this.updatedFertilizer = updatedFertilizer;
    }

    public int getUpdatedMoney() {
        return updatedMoney;
    }

    public void setUpdatedMoney(int updatedMoney) {
        this.updatedMoney = updatedMoney;
    }


}
